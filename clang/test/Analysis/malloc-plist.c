// Test that we refer to the last symbol used in the leak diagnostic.
void LeakedSymbol(int in) {
    int *m = 0;
    int *p;
    p = (int*)malloc(12);
    (*p)++;
    m = p;
    p = 0;
    (*m)++;
    in++;
}

